// Program 3 - Write a program to check that user is eligible for voting or not using ternary operator

class Program3 {
	public static void main(String[] args) {
		
		int age = 22;
		System.out.println((age > 18) ? "User is Eligible for voting" : "User is not Eligible for voting");

		age = 15;
		System.out.println((age > 18) ? "User is Eligible for voting" : "User is not Eligible for voting");


	}
}
