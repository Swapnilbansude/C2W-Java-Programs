

class IfElseLadderDemo {
	public static void main(String[] args) {
		int x = 25;

		if(x>=0 && x<10) {
			System.out.println("No bet 0 to 10");
		} else if(x>=10 && x<20) {
			System.out.println("No bet 10 to 20");
		} else if(x>=20 && x<30) {
			System.out.println("No bet 20 to 30");
		} else if(x>=30 && x<40) {
			System.out.println("No bet 30 to 40");
		} else if(x>=40 && x<50) {
			System.out.println("No bet 40 to 50");
		} else if(x>=50 && x<60) {
			System.out.println("No bet 50 to 60");
		}

	}
}
