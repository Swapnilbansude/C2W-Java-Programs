

class SwitchDemo2 {
	public static void main(String[] args) {
		byte x = 1;

		switch(x) {
			case 1:
				System.out.println("Ek");
			case 2:
				System.out.println("Don");
			case 3:
				System.out.println("Tin");
				break;
			case 4:
				System.out.println("Char");
			case 5:
				System.out.println("Pach");

		}
	}
}
