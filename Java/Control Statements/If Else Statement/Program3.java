/* Program 3- Write a java program to get any number and check whether the number is greater than
 * or less than or equal to another number
*/

class Program3 {
	public static void main(String[] args) {
		
		int num1 = 17;
		int num2 = 30;

		if(num1 < num2) {
			System.out.println(num1 + " is less than "+ num2);
		}
		
		if(num1 > num2) {
			System.out.println(num1 + " is greater than "+ num2);
		}

		if(num1 == num2) {
			System.out.println(num1 + " is equal to "+ num2);
		}



	}
}
